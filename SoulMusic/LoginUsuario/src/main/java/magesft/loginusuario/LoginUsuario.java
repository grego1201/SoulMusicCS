                //siguiente ventana
                System.out.println("correcto");
                String [] datos_u=arr.get(0);
                u=new Usuario(datos_u[0], datos_u[1], datos_u[2], Float.parseFloat(datos_u[3]), Integer.parseInt(datos_u[4]));
